// Default за неразпознат бизнес
$BIZ_DEFAULT_COEFF = 500;

/**
 * Намери най-близкия коефициент за даден бизнес текст.
 * Използва прост keyword matching — проверява дали ключ от масива се съдържа в текста.
 *
 * @param string $bizText — свободен текст от потребителя (напр. "продавам дамски дрехи и обувки")
 * @return array ['match' => 'дамски дрехи', 'coeff' => 600] или default
 */
function findBizCoefficient(string $bizText): array {
    global $BIZ_COEFFICIENTS, $BIZ_DEFAULT_COEFF;

    $text = mb_strtolower(trim($bizText));

    // 1. Точно съвпадение
    if (isset($BIZ_COEFFICIENTS[$text])) {
        return ['match' => $text, 'coeff' => $BIZ_COEFFICIENTS[$text]];
    }

    // 2. Най-дълъг ключ който се съдържа в текста (по-дълъг = по-специфичен)
    $bestMatch = '';
    $bestCoeff = $BIZ_DEFAULT_COEFF;
    foreach ($BIZ_COEFFICIENTS as $key => $coeff) {
        if (mb_strpos($text, $key) !== false && mb_strlen($key) > mb_strlen($bestMatch)) {
            $bestMatch = $key;
            $bestCoeff = $coeff;
        }
    }

    // 3. Обратно — ключ се съдържа ли в текста, или текстът в ключа
    if (!$bestMatch) {
        foreach ($BIZ_COEFFICIENTS as $key => $coeff) {
            if (mb_strpos($key, $text) !== false && mb_strlen($key) > mb_strlen($bestMatch)) {
                $bestMatch = $key;
                $bestCoeff = $coeff;
            }
        }
    }

    return ['match' => $bestMatch ?: 'друг ритейл бизнес', 'coeff' => $bestCoeff];
}

/**
 * Изчислява 5 WOW сценария с конкретни EUR суми.
 *
 * @param int $coeff — месечен коефициент на 1 обект
 * @param int $stores — брой обекти
 * @return array ['monthly' => X, 'yearly' => Y, 'zombie' => Z, 'sizes' => S, 'outofstock' => O, 'upsell' => U, 'discounts' => D]
 */
function calculateLosses(int $coeff, int $stores): array {
    $base = $coeff * $stores;
    return [
        'monthly'   => $base,
        'yearly'    => $base * 12,
        'zombie'    => round($base * 0.30),  // 30%
        'sizes'     => round($base * 0.20),  // 20%
        'outofstock'=> round($base * 0.25),  // 25%
        'upsell'    => round($base * 0.10),  // 10%
        'discounts' => round($base * 0.15),  // 15%
    ];
}
